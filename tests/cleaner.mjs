let pass=0,fail=0; const ok=(n,c,d)=>{if(c){pass++;console.log('  OK',n);}else{fail++;console.log('  FAIL',n,'|',d);}};
const clean = (str, opts) => page.evaluate(({str,opts})=>{ const D=window.__anomaly.DataCleaner; const o=Object.assign({}, D.DEFAULTS, opts||{}); const ctx=D.makeContext(); return {out:D.cleanString(str,o,ctx), ctx}; },{str,opts});
