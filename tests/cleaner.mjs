const INV='a'+String.fromCharCode(0x200B,0xFEFF)+'b';
const EMO='안녕'+String.fromCodePoint(0x1F600)+String.fromCodePoint(0x1F44D)+'!';
const EMO2='안녕'+String.fromCodePoint(0x1F600);
console.log('[기본 정제: trim/collapse/tab]');
ok('trim(기본)', (await clean('  hello  ',{})).out==='hello', '');
ok('collapse(기본)', (await clean('a    b',{})).out==='a b', '');
ok('tab(기본)', (await clean('a\tb',{})).out==='a b', '');
ok('줄바꿈은 기본 미적용', (await clean('a\nb',{})).out==='a\nb'||(await clean('a\nb',{})).out.indexOf('\n')>=0, JSON.stringify(await clean('a\\nb',{})));
console.log('[선택 옵션(명시적)]');
ok('줄바꿈 제거', (await clean('a\nb',{removeNewline:true})).out==='a b', '');
ok('제어문자 제거', (await clean(CTRL,{removeControl:true})).out==='ab', JSON.stringify(await clean(CTRL,{removeControl:true})));
ok('보이지 않는 문자 제거', (await clean(INV,{removeInvisible:true})).out==='ab', '');
const allc = await clean('a!@#b$%^&c()',{specialMode:'all'});
ok('모든 특수문자 제거(유지: % ( ) )', allc.out==='ab%c()', JSON.stringify(allc));
ok('특수문자 카운트', allc.ctx.specialRemoved>=6, JSON.stringify(allc.ctx));
ok('기본은 특수문자 유지', (await clean('a!@#b',{})).out==='a!@#b', '');
ok('이모지는 엔진 지원(옵션)', (await clean(EMO,{removeEmoji:true})).out==='안녕!', JSON.stringify(await clean(EMO,{removeEmoji:true})));
