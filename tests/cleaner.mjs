let c3=await cell('코드-123',{dashToZero:true},true); ok('code dash', c3.r==='코드-123', JSON.stringify(c3));
let c4=await cell('',{emptyToNull:true},false); ok('empty->null', c4.r===null, JSON.stringify(c4));
let c5=await cell(null,{nullToEmpty:true},false); ok('null->empty', c5.r==='', JSON.stringify(c5));
